namespace SystemHarness;

/// <summary>
/// Represents the visual state of a desktop window.
/// </summary>
public enum WindowState
{
    /// <summary>Normal (restored) state.</summary>
    Normal,

    /// <summary>Minimized to the taskbar.</summary>
    Minimized,

    /// <summary>Maximized to fill the screen.</summary>
    Maximized
}
